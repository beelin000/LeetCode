class L169 {
    public int majorityElement(int[] nums) {
        int candidate = nums[0];
        int voteCount = 1;
        for (int i=1; i< nums.length; i++) {
            int cur = nums[i];
            if (cur == candidate) {
                voteCount++;
            } else {
                voteCount--;
            }
            if (voteCount == 0) {
                candidate = cur;
                voteCount = 1;
            }
        }
        return candidate;
    }
}
