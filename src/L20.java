import java.util.Stack;

public class L20 {
    public boolean isValid(String s) {
        if(s == null || s.length() == 0) {
            return true;
        }
        if(s.length() % 2 != 0) return false;
        Stack<Character> stack = new Stack<>();
        // loop string
        for(char c:s.toCharArray()) {
            switch (c) {
                case '(':
                    stack.push('(');
                    break;
                case '[':
                    stack.push('[');
                    break;
                case '{':
                    stack.push('{');
                    break;
                case ')':
                    if(stack.pop() != '(') {
                        return false;
                    }
                    break;
                case ']':
                    if(stack.pop() != '[') {
                        return false;
                    }
                    break;
                case '}':
                    if(stack.pop() != '{') {
                        return false;
                    }
                    break;
                default:
            }
        }
        return stack.isEmpty();
    }
}
